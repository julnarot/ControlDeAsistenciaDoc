package upeu.rrhh.model;

public class Curso {

    private String cursoid;
    private String codigo;
    private String numcreditos;
    private String numhoras;
    private String nombre;

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getCursoid() {
        return cursoid;
    }

    public void setCursoid(String cursoid) {
        this.cursoid = cursoid;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getNumcreditos() {
        return numcreditos;
    }

    public void setNumcreditos(String numcreditos) {
        this.numcreditos = numcreditos;
    }

    public String getNumhoras() {
        return numhoras;
    }

    public void setNumhoras(String numhoras) {
        this.numhoras = numhoras;
    }
    
    
    
    
    
}