package upeu.rrhh.model;

public class Horas {

    private String horasid;
    private String horainicio;
    private String nomhora;
    private String turnoid;
    private String horafinal;

    public String getHorafinal() {
        return horafinal;
    }

    public void setHorafinal(String horafinal) {
        this.horafinal = horafinal;
    }

    public String getHorainicio() {
        return horainicio;
    }

    public void setHorainicio(String horainicio) {
        this.horainicio = horainicio;
    }

    public String getHorasid() {
        return horasid;
    }

    public void setHorasid(String horasid) {
        this.horasid = horasid;
    }

    public String getNomhora() {
        return nomhora;
    }

    public void setNomhora(String nomhora) {
        this.nomhora = nomhora;
    }

    public String getTurnoid() {
        return turnoid;
    }

    public void setTurnoid(String turnoid) {
        this.turnoid = turnoid;
    }
    
    
    
    
    
}
